const errorMsg = (message) => {
	toast.error(message);
}

const successMsg = (message) => {
	toast.success(message);
}

async function initializeWeb3() {
	try {

		const _web3 = await getWeb3()
		const _accounts = await _web3.eth.getAccounts()

		const _contract = await getContract(_web3, abi, ContractAddress)


		web3 = _web3;
		accounts = _accounts;
		contract = _contract;
		return { web3, accounts, contract }
	} catch (error) {
		errorMsg(
			`Failed to load web3, accounts, or contract. Check console for details.`
		)
		console.log(error)
	}
}

async function changeAccount(callback) {
	console.log('change account')
	window.ethereum.on('accountsChanged', function (_accounts) {
		// Time to reload your interface with accounts[0]!
		callback(_accounts)
	})
}

async function getById(token_id) {
	const token = await contract.methods.auctions(token_id.toString()).call()
	if (token.tokenId == '0') { return; }
	return token
}

async function getOwnerOf(token_id) {
	const owner = await contract.methods.ownerOf(token_id.toString()).call()
	return owner
}

async function bid(token_id, amount, account) {
	try {
		successMsg('Transaction is pending...')
		await contract.methods.bid(token_id).send({ from: account, value: amount * 10 ** 18 })
		successMsg('Transaction is successful')
	} catch (error) {
		errorMsg(error?.message ?? error)
	}
}

async function collect(token_id, account) {
	try {
		successMsg('Transaction is pending...')
		await contract.methods.collect(token_id).send({ from: account });
		successMsg('Transaction is successful')
	} catch (error) {
		errorMsg(error?.message ?? error)
	}
}




